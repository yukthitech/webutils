package com.yukthitech.webutils.common.models.auth;

import com.yukthitech.webutils.common.models.BaseResponse;

/**
 * Login Response object.
 * 
 * @author akiran
 */
public class LoginResponse extends BaseResponse
{
	/**
	 * Authorization token generated by server, that can be used for future
	 * requests.
	 */
	private String authToken;

	/**
	 * The user id, active login userId.
	 **/
	private Long userId;

	/**
	 * Instantiates a new login response.
	 */
	public LoginResponse()
	{}

	/**
	 * Instantiates a new login response.
	 *
	 * @param authToken
	 *            the auth token
	 * @param userId
	 *            the user id
	 */
	public LoginResponse(String authToken, Long userId)
	{
		this.authToken = authToken;
		this.userId = userId;
	}

	/**
	 * Instantiates a new login response.
	 *
	 * @param code
	 *            the code
	 * @param message
	 *            the message
	 */
	public LoginResponse(int code, String message)
	{
		super(code, message);
	}

	/**
	 * Gets the authorization token generated by server, that can be used for
	 * future requests.
	 *
	 * @return the authorization token generated by server, that can be used for
	 *         future requests
	 */
	public String getAuthToken()
	{
		return authToken;
	}

	/**
	 * Sets the authorization token generated by server, that can be used for
	 * future requests.
	 *
	 * @param authToken
	 *            the new authorization token generated by server, that can be
	 *            used for future requests
	 */
	public void setAuthToken(String authToken)
	{
		this.authToken = authToken;
	}

	/**
	 * Gets the user id.
	 *
	 * @return the user id
	 */
	public Long getUserId()
	{
		return userId;
	}

	/**
	 * Sets the user id.
	 *
	 * @param userId
	 *            the new user id
	 */
	public void setUserId(Long userId)
	{
		this.userId = userId;
	}
}
